<script setup>
import { ref, watch, nextTick, computed } from 'vue'
import axios from 'axios'
import catPlaceholder from '@/assets/cat-placeholder.png'
import { Chart, registerables } from 'chart.js'
Chart.register(...registerables)

// emit props
const emit = defineEmits(['close', 'toggle-favorite'])
const props = defineProps({
  property: {
    type: Object,
    default: null,
  },
  isOpen: {
    type: Boolean,
    default: false,
  },
  isFavorite: { type: Boolean, default: false },
  aptSeq: { type: String, required: true },
})

// refs
const activeTab = ref('info')
const loading = ref(false)
const error = ref('')
const detail = ref(null)
const schools = ref([])
const dealsData = ref([])
const dealsChart = ref(null)
const areaFilter = ref('all') // ÏÑ†ÌÉùÎêú Î©¥Ï†Å (‚Äòall‚Äô or Ïà´Ïûê)

// Îç∞Ïù¥ÌÑ∞Í∞Ä Î∞îÎÄî ÎïåÎßàÎã§ Í≥†Ïú† Î©¥Ï†Å Î™©Î°ùÏùÑ ÎΩëÏïÑÏÑú Ï†ïÎ†¨
const areaOptions = computed(() => {
  const set = new Set(dealsData.value.map((d) => Number(d.excluUseAr)))
  return Array.from(set).sort((a, b) => a - b)
})

// constants
const tabs = [
  { id: 'info', name: 'Ï†ïÎ≥¥' },
  { id: 'schools', name: 'ÌïôÍµê' },
]
let chartInstance = null

// ÏÇ¨Ïù¥ÎìúÎ∞î Ïó¥Î¶º/Îã´Ìûò Î°úÍ∑∏
watch(
  () => props.isOpen,
  (open) => {
    if (open) {
      console.log('üõà ÏÇ¨Ïù¥ÎìúÎ∞î Ïó¥Î¶º, aptSeq=', props.aptSeq)
    }
  },
)
// dealType Í∏∞Î∞òÏúºÎ°ú Î≥¥Ïó¨Ï§Ñ Í∞ÄÍ≤© Î¨∏ÏûêÏó¥ ÏÉùÏÑ±
const priceDisplay = computed(() => {
  console.log('üü¢ priceDisplay', detail.value)
  if (!detail.value) return '-'
  const { dealType, deposit, monthlyRent, latestPrice } = detail.value

  if (dealType === 'ÏõîÏÑ∏') {
    // deposit: Î≥¥Ï¶ùÍ∏à, monthlyRent: ÏõîÏÑ∏
    return `${formatCurrency(deposit)} / ${formatCurrency(monthlyRent)}`
  }
  // Ï†ÑÏÑ∏ÎÇò Îß§Îß§Îäî latestPrice ÌïÑÎìú ÏÇ¨Ïö©
  return formatCurrency(latestPrice)
})

watch(
  () => props.aptSeq,
  async (seq) => {
    if (!seq) return
    loading.value = true
    error.value = ''
    try {
      // (1) Í∏∞Ï°¥ detail & schools API Ìò∏Ï∂ú
      const [dRes, sRes, dealsRes] = await Promise.all([
        axios.get(`https://api.ssafy.blog/api/v1/house/${seq}/detail`),
        axios.get(`https://api.ssafy.blog/api/v1/house/${seq}/schools`),
        axios.get(`https://api.ssafy.blog/api/v1/house/${seq}/dealsDone`),
      ])
      detail.value = dRes.data
      schools.value = sRes.data
      dealsData.value = dealsRes.data
      console.log('üü¢ ÏÉÅÏÑ∏ Ï†ïÎ≥¥', detail.value)
    } catch (e) {
      console.error('üî¥ API Ïò§Î•ò', e)
      error.value = 'Îç∞Ïù¥ÌÑ∞Î•º Î∂àÎü¨Ïò§Îäî Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.'
    } finally {
      loading.value = false
      if (activeTab.value === 'info') {
        await drawChart()
      }
    }
  },
  { immediate: true },
)

// ÌÉ≠Ïù¥ info Î°ú Î∞îÎÄî Îïå
watch([activeTab, areaFilter], ([tab]) => {
  if (tab === 'info') {
    drawChart()
  }
})
async function drawChart() {
  if (activeTab.value !== 'info') return
  await nextTick()
  const canvas = dealsChart.value
  if (!canvas) return
  const ctx = canvas.getContext('2d')
  if (chartInstance) chartInstance.destroy()

  // Ï†ÑÏ≤¥ Í±∞Îûò ÎÇ¥Ïó≠ Ï§ë ÌïÑÌÑ∞ÎßÅ(Í∏àÏï°Ïù¥ ÏûàÎäî Í≤ÉÎßå)
  const allValid = dealsData.value.filter((d) => d.dealAmount)

  // (1) Í≥µÌÜµ ÎùºÎ≤®: YYYY-MM ÌòïÌÉúÎ°ú Ïú†ÎãàÌÅ¨ÌïòÍ≤å Ï†ïÎ†¨
  const allLabels = Array.from(
    new Set(allValid.map((d) => `${d.dealYear}-${String(d.dealMonth).padStart(2, '0')}`)),
  ).sort()

  const datasets = []

  if (areaFilter.value === 'all') {
    // (2) Î©¥Ï†Å ÏòµÏÖòÎßàÎã§ Îç∞Ïù¥ÌÑ∞ ÏÉùÏÑ±
    areaOptions.value.forEach((area) => {
      const filtered = allValid.filter((d) => Number(d.excluUseAr) === area)
      // ÎùºÎ≤®Ïóê ÎßûÏ∂∞ Í∞í Îß§Ìïë, ÏóÜÏúºÎ©¥ null
      const data = allLabels.map((label) => {
        const rec = filtered.find((d) => {
          const m = `${d.dealYear}-${String(d.dealMonth).padStart(2, '0')}`
          return m === label
        })
        return rec ? Number(rec.dealAmount.replace(/,/g, '')) : null
      })
      datasets.push({
        label: `${area}„é°`,
        data,
        fill: false,
        tension: 0.2,
      })
    })
  } else {
    // Îã®Ïùº Î©¥Ï†Å ÌïÑÌÑ∞ Î™®Îìú
    const filtered = allValid.filter((d) => Number(d.excluUseAr) === Number(areaFilter.value))
    const data = allLabels.map((label) => {
      const rec = filtered.find((d) => {
        const m = `${d.dealYear}-${String(d.dealMonth).padStart(2, '0')}`
        return m === label
      })
      return rec ? Number(rec.dealAmount.replace(/,/g, '')) : null
    })
    datasets.push({
      label: `${areaFilter.value}„é° Í±∞ÎûòÍ∞Ä`,
      data,
      fill: false,
      tension: 0.2,
    })
  }

  chartInstance = new Chart(ctx, {
    type: 'line',
    data: {
      labels: allLabels,
      datasets,
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      scales: {
        x: {
          title: { display: true, text: 'Í±∞Îûò Ïõî' },
        },
        y: { title: { display: true, text: 'Í∞ÄÍ≤©(ÎßåÏõê)' } },
      },
    },
  })
}

function closeSidebar() {
  emit('close')
}

function formatCurrency(val) {
  if (val == null) return '-'

  let eok = 0,
    man = 0,
    remainder = 0
  if (val >= 10000) {
    man = Math.floor(val / 10000)
    remainder = val % 10000
    if (man >= 10000) {
      eok = Math.floor(man / 10000)
      man = man % 10000
    }
  }

  return `${eok > 0 ? eok + 'Ïñµ' : ''}${man > 0 ? man + 'Îßå' : ''}${remainder != 0 ? remainder + 'Ïõê' : 'Ïõê'}`
}
